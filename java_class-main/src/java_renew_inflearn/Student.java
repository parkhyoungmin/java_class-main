package java_renew_inflearn;

public class Student {

	private String name;
	private int score;
	
	// 단축키 : Alt + Shift + s키
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	
}
