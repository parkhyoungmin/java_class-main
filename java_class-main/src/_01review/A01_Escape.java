package _01review;

public class A01_Escape {

	public static void main(String[] args) {
		System.out.println("Hello\nworld!");
//		Hello
//		world!
		System.out.println("안\t녕");
//		안	녕
		System.out.println("한\\영");
//		한\영
		System.out.println("\"큰따옴표\"");
//		"큰따옴표"
		System.out.println("\'작은따옴표\'");
//		'작은따옴표'
	}
	
}
